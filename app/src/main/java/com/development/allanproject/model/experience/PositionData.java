package com.development.allanproject.model.experience;

public class PositionData {
    private String position;
    private String speciality;
    private String unit;
    private String charting_technology;
    private boolean charge_experience;
    private String position_month;
    private String position_year;

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getSpeciality() {
        return speciality;
    }

    public void setSpeciality(String speciality) {
        this.speciality = speciality;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getCharting_technology() {
        return charting_technology;
    }

    public void setCharting_technology(String charting_technology) {
        this.charting_technology = charting_technology;
    }

    public boolean isCharge_experience() {
        return charge_experience;
    }

    public void setCharge_experience(boolean charge_experience) {
        this.charge_experience = charge_experience;
    }

    public String getPosition_month() {
        return position_month;
    }

    public void setPosition_month(String position_month) {
        this.position_month = position_month;
    }

    public String getPosition_year() {
        return position_year;
    }

    public void setPosition_year(String position_year) {
        this.position_year = position_year;
    }
}
